$.setCookie = function (name, value, option) {
    var str = name + '=' + escape(value);
    if (option) {
        if (option.expireHours) {
            var d = new Date();
            d.setTime(d.getTime() + option.expireHours * 3600 * 1000);
            str += '; expires=' + d.toGMTString();
        }
        if (option.path) str += '; path=' + option.path; else str += '; path=/';
        if (option.domain) str += '; domain=' + option.domain;
        if (option.secure) str += '; true';
    }
    document.cookie = str;
};

/* 省市跳转URL */
function JumpUrl(pinyin, id) {
    var area = id + '|' + pinyin;
    $.setCookie('uarea', area, { domain: 'm.2sc.sohu.com', expireHours: 24 * 30 });

    location.href = 'http://m.2sc.sohu.com/' + pinyin + '/';
}

var citySelectData = {};

function setJsonToData(data)
{
    var popupBox = "";
    if(data.c.length)
    {
        popupBox += '<li class="item-sub" data-index="' + data.i + '"><div class="w-dropdown01 w-dropdown01-col3"><div class="w-dropdown01-content">';
        popupBox += '<p><a href="http://m.2sc.sohu.com/' + data.en + '/">全部城市</a></p>';
        var cityList = data.c;
        var len = cityList.length;

        for(var i = 0; i < len; ++i)
        {
            popupBox += '<p><a href="http://m.2sc.sohu.com/' + data.en + '-' + cityList[i].en + '/" data-index="' + cityList[i].i + '">' + cityList[i].n + '</a></p>';
        }
        popupBox += '</div></div></li>';
        citySelectData[data.i] = popupBox;
    }
}

